package test;

import java.util.Scanner;

class buckyIncrement
{
	public static void main(String args []) {
		Scanner burger = new Scanner(System.in);
		
		int batman = 5;
		int butter= 99;
		++batman;
		--butter;
		burger.close();
	
		
		//*"+= and -= to add or subtract more than 1 number"
		
		batman -= 5;
		
		
		//*" ++ means adding 1 number")
		System.out.println(batman++);
		
		//* or just add in the Numberrrr
		System.out.println(butter - 52);
		
	
	}
	

}